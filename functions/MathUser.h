/*This File is a Header file of Math Relate Solution*/
#include <stdio.h>
int sum(int a, int b);
int sub(int a, int b);
float multiply(float x, float y);
float divide(float x, float y);

int sum(int a, int b)
{
    return a + b;
}

int sub(int a, int b)
{
    return a - b;
}

float multiply(float x, float y)
{
    return x * y;
}

float divide(float x, float y)
{
    return x / y;
}